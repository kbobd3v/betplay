const mongoose = require("mongoose");
const bcrypt = require('bcrypt');

// Con mongoose podemos usar sus schema para enviar la informacion a mongodb de manera adecuada
const userSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      required: true,
      default: false,
    },
    pic: {
      type: String,
      required: true,
      default:
        "https://icon-library.com/images/anonymous-avatar-icon/anonymous-avatar-icon-25.jpg",
    },
    balance: {
      type: Number,
      required: true,
      default: 50000,
    }
  },
  {
    timestamps: true,
  }
);

userSchema.methods.matchPassword = async function (enteredPassword) {
  return await bcrypt.compare(enteredPassword, this.password);
};

// will encrypt password everytime its saved
userSchema.pre("save", async function (next) {
  if (!this.isModified("password")) {
    next();
  }
  const salt = await bcrypt.genSalt(10);
  this.password = await bcrypt.hash(this.password, salt);
});

// Como siempre exportamos el model de mongoose, diciendo que se llamara User y su contenido sera le Schema que ya creamos
const User = mongoose.model("User", userSchema);

module.exports =  User;